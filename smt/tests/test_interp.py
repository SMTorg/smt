def test_interpolant_models():
    data = np.array(json.loads(DATA))

    fidelity = data[:, 2]
    x = data[:, 3:10]
    y = data[:, 10:13]

    for qoi_idx in range(3):
        x_doe = []
        y_doe = []

        for lvl in range(2):
            fid_mask = fidelity == lvl
            x_doe.append(x[fid_mask])
            y_doe.append(y[fid_mask, qoi_idx].reshape(-1, 1))

        dist = cdist(x_doe[0], x_doe[1])
        min_dist = np.min(dist, axis=0)
        if np.count_nonzero(min_dist) != 0:
            print("DOE is not nested.")

        # continuous KRG
        krg_cont = KRG(print_global=False, n_start=3, hyper_opt="Cobyla", seed=0)
        krg_cont.set_training_values(x_doe[1], y_doe[1])

        # mixed-variable KRG
        krg_mix = KRG(
            print_global=False,
            design_space=design_space,
            n_start=3,
            hyper_opt="Cobyla",
            seed=0,
        )
        krg_mix.set_training_values(x_doe[1], y_doe[1])

        # continuous MFK
        mfk_cont = MFK(print_global=False, n_start=3, hyper_opt="Cobyla", seed=0)
        mfk_cont.set_training_values(x_doe[0], y_doe[0], name=0)
        mfk_cont.set_training_values(x_doe[1], y_doe[1])

        # mixed-variable MFK
        mfk_mix = MFK(
            print_global=False,
            design_space=design_space,
            n_start=3,
            hyper_opt="Cobyla",
            seed=0,
        )
        mfk_mix.set_training_values(x_doe[0], y_doe[0], name=0)
        mfk_mix.set_training_values(x_doe[1], y_doe[1])

        _names = [
            "Continuous KRG",
            "Mixed-variable KRG",
            "Continuous MFK",
            "Mixed-variable MFK",
        ]
        models = [krg_cont, krg_mix, mfk_cont, mfk_mix]

        print(f"======= MSE at training points for QOI {qoi_idx + 1}/3 =======")
        for idx, model in enumerate(models):
            model.train()
            y_pred = model.predict_values(x_doe[-1])
            np.testing.assert_allclose(y_pred, y_doe[-1], rtol=1e-5, atol=1e-5)
